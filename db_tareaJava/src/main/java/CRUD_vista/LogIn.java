     public void validar(){
        int resultado = 0;
        try{
            String user = txt_username.getText();
            String pass = String.valueOf(txt_pass.getPassword());
            String query = "select * from tb_usuarios where username = '"+user+"' and password = '"+pass+"'";
            con = ClsConexion.getConnection();
            Statement stmt = con.createStatement();
            rs = stmt.executeQuery(query);
            if (rs.next()) {
                resultado = 1;
                if (resultado == 1) {
                    VendedoresFMR ventana = new VendedoresFMR();
                    ventana.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Usuario o contrase√±a incorrectos");
                }
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Algo ha salido mal en el acceso" + e.getMessage());
        }
    }

